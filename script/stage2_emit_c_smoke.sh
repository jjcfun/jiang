expect_fn_decl() {
    local ret="$1"
    local name="$2"
    local params="$3"
    local file="$4"
    rg -q "^${ret} .*${name}\\(${params}\\);$" "$file"
}

expect_type_start() {
    local kind="$1"
    local name="$2"
    local file="$3"
    rg -q "^typedef ${kind} .*${name} \\{$" "$file"
}

expect_type_end() {
    local name="$1"
    local file="$2"
    rg -q "^} .*${name};$" "$file"
}
